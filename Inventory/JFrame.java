package JavaProjet.Inventory;

public class JFrame {
    String Title;
    int Size;
    boolean DefaultClose;
    boolean Visibility;

    public JFrame(String Title, int Size, boolean DefaultClose, boolean Visibility) {
        this.Title = Title;
        this.Size = Size;
        this.DefaultClose = DefaultClose;
        this.Visibility = Visibility;
    }
}
